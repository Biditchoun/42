#include <stdio.h>
#include <fcntl.h>
#include "get_next_line.h"

int	main()
{
	int	fd;
	int i;
	char *line;

	fd = open("text", O_RDONLY);
	i = 1;
	while (i)
	{
		line = get_next_line(fd);
		printf("Main : %s", line);
		if (!line)
			i = 0;
		free(line);
	}
	close(fd);
	return (0);
}
