int	ft_iterative_factorial(int nb)
{
	int	n;

	if (!nb)
		nb = 1;
	if (nb < 0 || nb > 13)
		nb = 0;
	n = nb;
	while (n > 1)
		nb = --n * nb;
	return (nb);
}
