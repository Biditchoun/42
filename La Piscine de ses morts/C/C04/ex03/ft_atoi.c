int	ft_atoi(char *str)
{
	int	i;
	int	m;
	int	nb;

	i = 0;
	m = 0;
	nb = 0;
	if (!str)
		return (0);
	while (str[i] == 32 || (str[i] > 8 && str[i] < 14))
		i++;
	while (str[i] == '+' || str[i] == '-')
		if (str[i++] == '-')
			m++;
	while (str[i] >= '0' && str[i] <= '9')
		nb = nb * 10 + str[i++] - '0';
	if (m % 2 == 1)
		nb = -nb;
	return (nb);
}
