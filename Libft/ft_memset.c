#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t	i;
	char	*a;

	if (!s)
		return (s);
	a = (char *)s;
	i = 0;
	while (i < n)
		a[i++] = c;
	return (s);
}
